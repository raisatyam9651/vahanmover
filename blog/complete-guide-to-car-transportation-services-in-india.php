            <h2>3. How to Prepare Your Car for Transport (Checklist)</h2>
            <p>
                To avoid transit issues and prepare your car for logistics handlers, execute the following steps 24 hours before pickup:
            </p>
            <ol>
                <li><strong>Clean the Car Thoroughly:</strong> Wash the exterior and vacuum the interior. A clean car makes it easy to spot pre-existing scratches or dents during the initial inspection.</li>
                <li><strong>Remove All Personal Items:</strong> Empty the glove box, boot space, and seats. Remove GPS mounts, dash cams, perfumes, and loose speakers to avoid loss or theft, as transporters are not liable for items left inside the cabin.</li>
                <li><strong>Maintain 1/4th Fuel Level:</strong> Do not top up your tank. The car only needs enough fuel to be driven onto the carrier ramp and off at the destination. Keeping the fuel level low reduces overall weight and minimizes safety risks.</li>
                <li><strong>Check Tyres and Fluid Leaks:</strong> Ensure tires have correct air pressure. Check the undercarriage for any fluid leaks (oil or coolant) to prevent damage to vehicles loaded below yours on double-deck carriers.</li>
                <li><strong>Deactivate Car Alarm:</strong> Turn off security alarms to prevent the car battery from draining if the alarm is triggered by road vibrations.</li>
                <li><strong>Fold Side Mirrors and Antennas:</strong> Retract external antennas and fold side-view mirrors inward to prevent them from snapping during tight loading configurations.</li>
            </ol>

            <div class="alert-box">
                <i class="fa-solid fa-camera"></i> <strong>STEP-BY-STEP PRE-INSPECTION ADVICE:</strong><br>
                Take high-definition photos and video walkthroughs of your car from all angles in broad daylight. Capture pre-existing scratches, alloy wheel conditions, and odometer readings. Share this with the transporter and ensure they document it on the <strong>Car Condition Report</strong> before signing the loading slip.
            </div>

            <h2>4. Checking for Verified Transporters</h2>
            <p>
                Never choose a car transporter solely based on the lowest quote. A cheap operator might park your car in open yards, delay delivery, or operate without proper registration. Ensure you:
            </p>
            <ul>
                <li>Verify GST registration and physical office addresses.</li>
                <li>Check online Google Reviews and ask for past customer references.</li>
                <li>Confirm they use their own vehicle carriers instead of subcontracting.</li>
                <li>Review the transit insurance document carefully for policy coverage terms.</li>
            </ul>


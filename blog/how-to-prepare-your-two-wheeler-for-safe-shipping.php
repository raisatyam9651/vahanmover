        <div class="glass-card article-content" style="padding: 50px; max-width: 900px; margin: -50px auto 0; position: relative; z-index: 10;">
            
            <p>
                When packing and moving your bike, relying entirely on the logistics provider is a common mistake. Even the best packers and movers require you to perform basic preparations on your vehicle to comply with transit laws and protect delicate mechanical parts from damage.
            </p>
            
            <p>
                By dedicating 30 minutes to prepare your two-wheeler, you can ensure it rolls off the container ramp at the destination in the exact same condition it was picked up. Here are the 6 vital steps to follow.
            </p>

            <h2>Step 1: Clean the Vehicle Thoroughly</h2>
            <p>
                Take your bike to a professional washing center for a deep clean before the shipping day. 
            </p>
            <ul>
                <li><strong>Why it matters:</strong> Dirt, dust, and dried mud can easily hide pre-existing scratches, paint chips, and small cracks on the engine casing or frame.</li>
                <li><strong>Action:</strong> Inspect the clean body panels alongside the transporter's supervisor. Mark any pre-existing blemishes on the consignment note so you have an accurate visual baseline.</li>
            </ul>

            <h2>Step 2: Draining the Fuel Tank</h2>
            <p>
                Depending on your transport medium, you must manage your fuel level:
            </p>
            <ul>
                <li><strong>Railways booking:</strong> Draining the petrol tank to <strong>100% dry</strong> is legally mandatory. If fuel is found, you will face heavy fines under railway safety acts.</li>
                <li><strong>Private container carriers:</strong> Draining all fuel is not mandatory, but you must keep the tank **at less than 1 liter** (essentially reserve fuel level). Keeping the fuel low prevents vapors and leakage during bumpy mountain routes or steep loading angles.</li>
            </ul>

            <h2>Step 3: Remove Loose Accessories & Mirrors</h2>
            <p>
                Loose accessories are highly vulnerable to breaking under the pressure of tight packing ropes or surrounding cargo movement.
            </p>
            <ol>
                <li><strong>Rearview Mirrors:</strong> Unscrew both side-view mirrors. Wrap them in bubble wrap and carry them separately in your luggage.</li>
                <li><strong>Mobile Mounts & USB Chargers:</strong> Detach phone holders, mounts, external horns, and aftermarket accessories.</li>
                <li><strong>Helmets and Crash Guards:</strong> Empty the boot storage space under the seat. If you have soft cargo saddlebags, remove them. Leave only the tool kit and vehicle documents inside.</li>
            </ol>

            <div class="alert-box">
                <i class="fa-solid fa-circle-exclamation"></i> <strong>HANDLEBAR LOCKING NOTE:</strong><br>
                Do not engage the handlebar lock (steering lock) when hand-overing the bike. The handlers must be able to steer the vehicle to roll it up the ramps and arrange it inside the truck. Leave the key in the ignition or hand it to the supervisor.
            </div>

            <h2>Step 4: Check Tyre Pressure & Mechanical Settings</h2>
            <p>
                Make sure your bike is in a rollable condition. 
            </p>
            <ul>
                <li><strong>Tyres:</strong> Inflate the tires to their recommended PSI level. Flat tires make the bike difficult to push and steer, increasing the risk of handlers dropping the vehicle.</li>
                <li><strong>Fluid Leaks:</strong> Check for any engine oil or brake fluid leaks. Alert the shipping company if the vehicle has active leaks so they do not place it directly above other vehicles.</li>
                <li><strong>Battery:</strong> For long-distance relocation exceeding 1,000 km, ask the supervisor if they will disconnect the battery terminals to prevent discharge during transit.</li>
            </ul>

            <h2>Step 5: Record the Vehicle's Condition (Documenting)</h2>
            <p>
                Never hand over your keys without documentation. Take photos and videos of the vehicle on the day of pickup:
            </p>
            <ul>
                <li>Take close-up shots of the exhaust pipe, silencer chrome, speedometer dial, side mirrors, and fuel tank.</li>
                <li>Record a quick video walkthrough showing that the engine starts smoothly.</li>
                <li>Confirm the odometer reading is recorded on the transit copy.</li>
            </ul>

            <h2>Step 6: Keep a Set of Keys and Document Copies Ready</h2>
            <p>
                Create a folder containing copies of your RC book, insurance, and PUC certificate. You must hand over these copies to the transporter during loading.
            </p>
            <p>
                <strong>Never hand over your master key.</strong> Share a duplicate key set with the transporter for ignition and boot access. Keep the master key safely with you.
            </p>

            <h2>Conclusion</h2>

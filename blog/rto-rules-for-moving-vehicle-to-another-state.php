        <div class="glass-card article-content" style="padding: 50px; max-width: 900px; margin: -50px auto 0; position: relative; z-index: 10;">
            
            <p>
                Under the Indian Motor Vehicles Act, a vehicle registered in one state can run in another state for a <strong>maximum period of 12 months (1 year)</strong> without re-registration. If you plan to move your car or bike permanently, or for a duration exceeding one year, you must register the vehicle at the local RTO of the new state and pay the corresponding road tax.
            </p>
            
            <p>
                Navigating the RTO bureaucracy can feel overwhelming. In this guide, we simplify the four major legal steps: getting a No Objection Certificate (NOC), paying road tax in the new state, re-registering your vehicle, and claiming a road tax refund from your old state.
            </p>

            <h2>Step 1: Procuring the NOC from the Parent RTO</h2>
            <p>
                Before moving your vehicle, you must obtain a <strong>No Objection Certificate (NOC)</strong> from the RTO where your vehicle is currently registered. The NOC certifies that there are no pending traffic challenges, criminal cases, or unpaid taxes on the vehicle.
            </p>

            <h3>How to apply for NOC:</h3>
            <ul>
                <li>Obtain and fill out <strong>RTO Form 28</strong> (in triplicate).</li>
                <li>If the vehicle has an active loan (hypothecation), you must first obtain a physical NOC from your bank/financier using <strong>Form 35</strong>.</li>
                <li>Submit the application at your parent RTO along with:
                    <ul>
                        <li>Certified copy of the Registration Certificate (RC).</li>
                        <li>Copy of valid Insurance Policy.</li>
                        <li>Pollution Under Control (PUC) certificate.</li>
                        <li>Chassis print of the vehicle.</li>
                        <li>Police clearance report from the local Crime Records Bureau (to check if the vehicle is involved in theft).</li>
                    </ul>
                </li>
            </ul>
            <p>
                The parent RTO will issue the NOC in the name of the destination RTO where you plan to register the vehicle.
            </p>

            <h2>Step 2: Road Tax Payment in the Destination State</h2>
            <p>
                Once you shift your vehicle to the new state, you must visit the local RTO and pay the lifetime road tax. The tax rate varies by state and is calculated based on:
            </p>
            <ul>
                <li>The age of the vehicle (depreciated value).</li>
                <li>The engine capacity (cc for bikes, displacement for cars).</li>
                <li>Fuel type (petrol, diesel, or electric).</li>
            </ul>
            <p>
                Ensure you submit the road tax fee through the RTO portal (Vahan dashboard) or directly at the RTO cash counter. Keep the receipt safe, as it is mandatory to claim a tax refund from your previous state.
            </p>

            <div class="alert-box">
                <i class="fa-solid fa-circle-info"></i> <strong>EXEMPTION FOR TEMPORARY TRANSFERS:</strong><br>
                If you are relocating for less than one year, you do not need an NOC or re-registration. However, keep documents like your transport receipt, toll receipts, or office relocation letter in your vehicle to prove to local traffic police the date on which your vehicle entered the state.
            </div>

            <h2>Step 3: Re-Registration of the Vehicle (New Number Plate)</h2>
            <p>
                After paying the road tax, you must apply for a new registration mark (e.g., getting an MH plate instead of DL). The application must be filed within the 12-month period.
            </p>
            <h3>Required Documents:</h3>
            <ol>
                <li>Original NOC from the parent RTO.</li>
                <li>Copy of the original RC.</li>
                <li>Form 27 (Application for assignment of new registration mark).</li>
                <li>Form 29 & 30 (Only if vehicle ownership is changing).</li>
                <li>New road tax payment receipt.</li>
                <li>Local address proof in the new state (Rent agreement, utility bill, or employer letter).</li>
                <li>Valid Insurance & PUC certificates.</li>
            </ol>
            <p>
                Upon submission and inspection of the chassis number, the RTO will issue a new Smart Card RC with the new state registration number.
            </p>

            <h2>Step 4: Claiming Road Tax Refund from Old RTO</h2>
            <p>
                Since you paid lifetime road tax (15 years) in your old state and have now paid it again in the new state, you are legally entitled to a pro-rata refund of the remaining tax years from the parent RTO.
            </p>
            <h3>How to claim the tax refund:</h3>
            <ul>
                <li>Submit an application for refund to your old RTO.</li>
                <li>Attach **Form DT** (Application for refund of tax).</li>
                <li>Provide the **new RC** showing the new registration number.</li>
                <li>Provide the **old RC copy** and the **new state road tax receipt**.</li>
                <li>Submit a bank cancel check copy where you want the refund amount credited.</li>
            </ul>
            <p>
                <em>Note: Processing road tax refunds can take several months and requires regular follow-ups at the parent RTO.</em>
            </p>

            <div class="alert-box" style="background: rgba(16, 185, 129, 0.1); border-left-color: #10b981;">
                <i class="fa-solid fa-star" style="color: #10b981;"></i> <strong>THE NEW BH (BHARAT) SERIES OPTION:</strong><br>
                To avoid the hassle of NOCs and tax refunds, the Government of India introduced the **BH (Bharat) Series** registration. If you are a defense personnel or a private sector employee with offices in 4 or more states, you can get a BH plate. Vehicles with BH plates do not need an NOC or re-registration when moving across states, and road tax is paid in 2-year blocks online.
            </div>


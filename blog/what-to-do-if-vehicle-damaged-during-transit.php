            <p>
                Handing over your car or bike keys to a logistics service requires trust. Despite professional packing and secure trailers, unforeseen transit challenges (such as road accidents, major bumps, or bad loading) can occasionally cause scratches, dents, or alignment issues.
            </p>
            
            <p>
                In this guide, we outline the step-by-step actions you must take immediately upon receiving a damaged vehicle to secure financial compensation.
            </p>

            <h2>1. Inspect the Vehicle Before Signing Delivery Receipts</h2>
            <p>
                The most critical rule of vehicle shipping is: **Never sign the final delivery receipt (consignment note) before a thorough inspection.**
            </p>
            <ul>
                <li><strong>Daylight Verification:</strong> If the vehicle is delivered after dark, park it under bright streetlights or inspect it with your smartphone flashlight.</li>
                <li><strong>Compare Photos:</strong> Open the pickup photos on your phone and compare them side-by-side with the current state of the bumpers, mirrors, handles, and side panels.</li>
            </ul>

            <h2>2. Document the Damages Immediately</h2>
            <p>
                If you locate new damage:
            </p>
            <ol>
                <li><strong>Write it Down:</strong> Write a detailed description of the damage on the transporter's delivery receipt copy before signing it. Do not accept a clean receipt.</li>
                <li><strong>Take Photos/Videos:</strong> Click clear close-up photos and wide-angle videos of the scratches, dents, or cracked indicators. Ensure the transporter's truck license plate is visible in some pictures to prove the context.</li>
                <li><strong>Obtain Driver Acknowledgment:</strong> Make the delivery driver sign a note acknowledging that the damage occurred in transit.</li>
            </ol>

            <div class="alert-box">
                <i class="fa-solid fa-circle-exclamation"></i> <strong>IMPORTANT LEGAL ADVICE:</strong><br>
                Avoid verbal promises from transport drivers. Drivers are third-party contract workers and have no authority to settle claims. All damages must be documented on the physical copy of the consignment receipt.
            </div>

            <h2>3. File an Insurance Claim</h2>
            <p>
                Most reputable movers purchase transit insurance from agencies. To initiate a claim:
            </p>
            <ul>
                <li>Contact the transporter's main office with the booking ID, delivery receipt copy, and damage images.</li>
                <li>Get a repair estimation invoice from an authorized dealership or workshop.</li>
                <li>Submit the estimation invoice along with the claim application to the insurance agency to process the payout.</li>
            </ul>


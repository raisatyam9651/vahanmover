            <p>
                When moving your vehicle, you might trust the driver, but road accidents, landslides, fire, structural carrier damage, or cargo shifting inside the truck are beyond anyone's control. A minor dent on a premium car or a broken clutch lever on a sports motorcycle can lead to expensive repairs.
            </p>
            
            <p>
                This is where **Vehicle Transit Insurance** becomes essential. In this guide, we explain the difference in coverage types, how premiums are calculated, and the exact steps to file a successful claim in case of damage.
            </p>

            <h2>1. What is Transit Insurance?</h2>
            <p>
                Unlike your standard comprehensive motor insurance (which covers active driving on public roads), transit insurance specifically covers the vehicle <strong>while it is being loaded, transported on a carrier truck, and unloaded</strong>. 
            </p>
            <p>
                Many vehicle owners assume their active car insurance covers transit. However, standard policies often have clauses that exclude coverage when the vehicle is in commercial transport custody, making a separate transit policy mandatory.
            </p>

            <h2>2. Types of Transit Insurance Coverage</h2>
            <p>
                When booking your transporter, verify the exact policy type they are offering:
            </p>

            <h3>All-Risk Transit Insurance (Recommended)</h3>
            <p>
                This policy covers any form of damage that occurs to the vehicle during the relocation process, including scratches, body panel dents, structural damages, collisions, theft, and fire.
            </p>
            <ul>
                <li><strong>Premium:</strong> Usually ranges between **0.2% and 0.3%** of the declared vehicle value.</li>
                <li><strong>Coverage:</strong> Comprehensive, from pickup point to delivery gate.</li>
            </ul>

            <h3>Restricted Cover (Fire & Collision Only)</h3>
            <p>
                This limited policy pays out only if the vehicle carrier truck meets with an active accident (collision, turning over) or catches fire. 
            </p>
            <ul>
                <li><strong>Premium:</strong> Lower than all-risk cover (often around 0.1%).</li>
                <li><strong>Coverage:</strong> It will NOT pay out for cosmetic damages like side scratches, mirror breaks, or denting caused by poor cargo strapping.</li>
            </ul>

            <h2>3. How is the Declared Vehicle Value Calculated?</h2>
            <p>
                To avoid disputes during claims, the value of your vehicle must be declared accurately on the consignment note. 
            </p>
            <ul>
                <li><strong>IDV Reference:</strong> The declared value should closely match the Insured Declared Value (IDV) stated in your active vehicle insurance policy.</li>
                <li><strong>Under-insuring:</strong> Declaring a lower value to save on the 0.2% premium is a risky mistake. In case of total loss or theft, the insurance company will only pay the declared amount.</li>
                <li><strong>Over-insuring:</strong> Declaring a value higher than the market rate will be flagged by surveyors, and the claim settlement will be restricted to the actual market depreciated value anyway.</li>
            </ul>

            <div class="alert-box">
                <i class="fa-solid fa-circle-exclamation"></i> <strong>THE PREMIUM IS AN ADD-ON:</strong><br>
                Ensure the transit insurance premium is listed as a separate, distinct line item on your logistics invoice. Ensure the transporter issues a valid **Marine Insurance Certificate** (transit policies fall under marine cargo clauses) containing your vehicle's engine and chassis numbers.
            </div>

            <h2>4. Step-by-Step Claim Filing Process</h2>
            <p>
                If your vehicle arrives at the destination with damages, do not panic. Execute the following steps to secure your claim settlement:
            </p>

            <h3>1. Do Not Sign the Delivery Slip Immediately</h3>
            <p>
                Transporters will present a delivery receipt (consignment note) for you to sign. If you sign it without comment, you are acknowledging that the vehicle arrived in perfect condition. Once signed, filing a claim becomes nearly impossible.
            </p>
            <p>
                Instead, write a comment on the physical slip: <strong>"Received with damage [specify the damage, e.g., left door dent]"</strong>, and take a photo of the slip.
            </p>

            <h3>2. Document the Damage (Photos and Videos)</h3>
            <p>
                Take high-definition photos and video walkthroughs of the damaged areas immediately in the presence of the delivery driver. Make sure the carrier truck plate is visible in the background if possible.
            </p>

            <h3>3. Request a Damage Certificate</h3>
            <p>
                Ask the delivery supervisor to issue a signed **Damage Certificate** on the company's letterhead, acknowledging the transit damage.
            </p>

            <h3>4. Contact the Insurance Provider & Surveyor</h3>
            <p>
                Call the insurance broker or the company directly within <strong>24 to 48 hours</strong>. The insurance company will appoint a local surveyor to inspect the vehicle at your address or workshop.
            </p>

            <h3>5. Submit Documents for Settlement</h3>
            <p>
                Keep these documents ready to submit to the surveyor:
            </p>
            <ul>
                <li>Original consignment note and bill showing the insurance premium paid.</li>
                <li>Marine Insurance Certificate copy.</li>
                <li>RC copy, DL copy, and base insurance copy.</li>
                <li>Pre-inspections photos (proving the damage was not there before).</li>
                <li>Repair estimation invoice from an authorized service center.</li>
            </ul>


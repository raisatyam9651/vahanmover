            <h2>1. Luggage Booking vs. Parcel Booking</h2>
            <p>
                Indian Railways offers two distinct methods to book and transport a vehicle. Choosing the right one depends on whether you are traveling on the same train or sending the bike independently.
            </p>

            <h3>Luggage Booking (Traveling on Same Train)</h3>
            <p>
                If you are traveling to the destination and hold a valid, confirmed train ticket, you can book your two-wheeler as <strong>luggage</strong>. 
            </p>
            <ul>
                <li><strong>Arrival:</strong> The bike is loaded onto the luggage van of the same train you are traveling on.</li>
                <li><strong>Delivery:</strong> You can claim and receive delivery immediately upon arriving at your destination station by presenting your luggage ticket.</li>
                <li><strong>Cost:</strong> Slightly higher than parcel booking, but ensures you and your vehicle arrive together.</li>
            </ul>

            <h3>Parcel Booking (Sending Independently)</h3>
            <p>
                If you are not traveling or do not have a train ticket, you must book the vehicle as a <strong>parcel</strong>.
            </p>
            <ul>
                <li><strong>Transit:</strong> The railway authorities will load your bike onto any available passenger or cargo train heading towards your destination route.</li>
                <li><strong>Delivery:</strong> It usually takes 3 to 7 days for the parcel to reach the destination station. You must track it and claim it within 24 hours of arrival to avoid storage charges (demurrage).</li>
                <li><strong>Cost:</strong> Slightly cheaper, but requires regular follow-ups.</li>
            </ul>

            <h2>2. Mandatory Documents Checklist</h2>
            <p>
                You must present original copies of the following documents to the booking clerk at the railway station parcel office, along with two sets of photocopies:
            </p>
            <ol>
                <li><strong>Registration Certificate (RC Book):</strong> The vehicle must have a valid Registration Certificate. Note that the name on the RC should ideally match the sender's ID, or you must provide an authorization letter from the owner.</li>
                <li><strong>Valid Insurance Policy Document:</strong> Required to calculate the transit insurance fee.</li>
                <li><strong>Government Issued ID Proof:</strong> Aadhaar Card, Driving License, or Voter ID card of the sender.</li>
                <li><strong>Confirmed Train Ticket:</strong> Mandatory ONLY if you are booking under Luggage rules.</li>
            </ol>

            <div class="alert-box">
                <i class="fa-solid fa-circle-exclamation"></i> <strong>CRITICAL RTO RULE FOR TRANSPORTATION:</strong><br>
                Make sure the bike's registration is active and the engine/chassis numbers match the RC book description. The railway police reserves the right to inspect and reject vehicles with modified/scratched chassis numbers to prevent the relocation of stolen property.
            </div>

            <h2>3. Preparing Your Bike: The Fuel Rule</h2>
            <p>
                Before handing over the bike to the packing agents, you must prepare it to meet railway safety guidelines.
            </p>

            <h3>The Zero-Fuel Policy</h3>
            <p>
                As per Indian Railways safety regulations, the <strong>fuel tank of the vehicle must be completely empty</strong>. Draining all petrol is mandatory to prevent accidental fires inside the luggage coach during transit.
            </p>
            <p>
                Before loading, the parcel staff will inspect your tank by shaking the vehicle or using a tube to verify there is zero fuel. 
            </p>
            
            <div class="alert-box" style="background: rgba(239, 68, 68, 0.1); border-left-color: #ef4444;">
                <i class="fa-solid fa-triangle-exclamation" style="color: #ef4444;"></i> <strong>HEAVY FINES FOR VIOLATION:</strong><br>
                If any fuel is detected in your vehicle's tank during inspection or transit, you can be penalized under <strong>Section 164 of the Indian Railways Act</strong>. The fine ranges from <strong>₹1,000 to ₹5,000</strong>, and you may face legal charges for endangering public safety.
            </div>

            <h2>4. Packing Guidelines at the Station</h2>
            <p>
                While you can pack the bike yourself, it is recommended to hire the professional packing agents available outside the station's parcel gate. Proper packing prevents scratches and denting:
            </p>
            <ul>
                <li><strong>Bubble Wrap Layer:</strong> Apply thick bubble wrap around the headlamps, tail lamps, turn indicators, and digital instrument clusters.</li>
                <li><strong>Cardboard Shield:</strong> Secure cardboard sheets around the petrol tank and side panels to prevent scratches from surrounding luggage.</li>
                <li><strong>Gunny Bag Wrap:</strong> Wrap the entire bike structure in a gunny bag (jute bag) and sew it securely.</li>
                <li><strong>Marking:</strong> Clearly write the booking receipt number (Railway Receipt - RR), sender's name, destination station code, and receiver's phone number on the gunny wrap using a permanent marker.</li>
            </ul>

